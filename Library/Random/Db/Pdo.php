namespace Random\Db;

use Random\IDatabase;

class Pdo extends Db implements IDatabase
{

    protected $transactionCounter = 0;
    protected $transactionError = array();

    function __construct($host, $username, $password, $database, $port = 3306)
    {
        parent::__construct($host, $username, $password, $database, $port = 3306);
        $this->conn = $this->connect($this->host, $this->username, $this->password, $this->database, $this->port);
    }

    function connect($host, $username, $password, $database, $port = 3306)
    {
        if (!isset($this->conn)) {
            $dsn = "mysql:host=$host;dbname=$database";
            $this->conn = new \PDO($dsn, $username, $password);
            if ($this->conn->errorCode() == '00000') {
                die($this->conn->errorInfo()[2]);
            }
            $this->conn->exec('set names utf8');
        }
        return $this->conn;
    }

    /**
     * @author DJM <op87960@gmail.com>
     * @param $result mixed
     * @todo 请求后更新成员变量$_error等
     */
    private function updateField($result)
    {
        if ($result) {
            $this->_affectedRows = $result->rowCount();
            $this->_fieldCount = $result->columnCount();
            $this->_insertId = $this->conn->lastInsertId();
        } else {
            $this->_affectedRows = 0;
            $this->_fieldCount = 0;
            $this->_insertId = 0;
            $this->_error = $this->conn->errorInfo()['2'];
        }
    }

    public function query($sql)
    {
        $result = parent::query($sql); // TODO: Change the autogenerated stub
        if ($this->transactionCounter && !$result) {
            $this->transactionError["'$this->transactionCounter'"] = true;
        }
        $this->updateField($result);
        return $result;
    }

    public function getRow($sql)
    {
        $result = $this->query($sql);
        $result->setFetchMode(\PDO::FETCH_ASSOC);
        $arr = $result->fetch();
        return $arr;
    }

    public function getArray($sql)
    {

        $result = $this->query($sql);
        if ($result) {
            $result->setFetchMode(\PDO::FETCH_ASSOC);
            $result = $result->fetchAll();
        }
        return $result;
    }

    public function beginTransaction()
    {

        if (!$this->transactionCounter++) {
            $this->transactionError["'$this->transactionCounter'"] = false;
            return $this->conn->beginTransaction();
        }
        $this->conn->exec('SAVEPOINT trans' . $this->transactionCounter);
        $this->transactionError["'$this->transactionCounter'"] = false;
        return $this->transactionCounter >= 0;
    }

    public function endTransaction()
    {
        if ($this->transactionError["'$this->transactionCounter'"]) {
            $result = $this->rollback();
        } else {
            $result = $this->commit();
        }
        unset($this->transactionCounter["'$this->transactionCounter'"]);
        return $result;
    }

    public function commit()
    {
        if (!--$this->transactionCounter) {
            return $this->conn->commit();
        }
        return $this->transactionCounter >= 0;
    }

    public function rollback()
    {
        if (--$this->transactionCounter) {
            $this->conn->exec('ROLLBACK TO trans' . ($this->transactionCounter + 1));
            return true;
        }
        return $this->conn->rollback();
    }
}
